package th.co.ais.sms.service;

public class SMSModel {
	private String userName;
	private String urType;
	private String urNo;
	private String smsNo;
	private String smsWording;
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUrType() {
		return urType;
	}
	public void setUrType(String urType) {
		this.urType = urType;
	}
	public String getUrNo() {
		return urNo;
	}
	public void setUrNo(String urNo) {
		this.urNo = urNo;
	}
	public String getSmsNo() {
		return smsNo;
	}
	public void setSmsNo(String smsNo) {
		this.smsNo = smsNo;
	}
	public String getSmsWording() {
		return smsWording;
	}
	public void setSmsWording(String smsWording) {
		this.smsWording = smsWording;
	}
	
	
}
